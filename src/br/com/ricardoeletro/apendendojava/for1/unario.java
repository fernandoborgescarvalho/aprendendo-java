
public class Unario {

    public static void main(String[] args) {
        System.out.println("+1 : " + +1);
        System.out.println("-3 : " + -3);

        int x = 0;
        System.out.println("x++ : " + x++);
        System.out.println("++x : " + ++x);

        int y = 3;
        System.out.println("y++ : " + y--);
        System.out.println("++y : " + --y);

        boolean b = false;
        System.out.println("b : " + b);
        System.out.println("b : " + b);

        int z = 0;
        
        System.out.println("Z : " + (++z ));
        
    }
}



