package br.com.ricardoeletro.aprendendojava.while1;

public class Switch {

    public static void main(String[] args) {

        Integer i = 0;
        switch (i) {
            case 0:
                System.out.println("case 0");
                break;
            case 1:
                System.out.println("case 1");
                break;
            default:
                System.out.println("default");
        }

    }

}
