package br.com.ricardoeletro.aprendendojava.while1;

/**
 *
 * @author fernando.carvalho
 */
public class While {

    public static void main(String[] args) {
        int i = 0;
        while (i < 10) {
            System.out.println("i : " + i);
            i++;
        }

        for (int x = 0; x < 10; x++) {
            System.out.println("i : " + i);

        }
    }
}
