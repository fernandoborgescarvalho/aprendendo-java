
package br.com.ricardoeletro.aprendendojava.array;


public class ArrayMultidimensional {
    
    
    public static void main(String[] args) {
        int[][] array = new int[2][2];
        array[0][0] = 0;
        array[0][1] = 1;
        array[1][0] = 10;
        array[1][1] = 11;
        
        System.out.println("array[1][0] : "+array[1][0]);
    }
    
}
